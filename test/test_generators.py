@pytest.mark.parametrize("start, end, expected_count", [
    (1, 5, 5),
    (10, 15, 6),
    (1, 1, 1)
])
def test_card_number_generator(start, end, expected_count):
    card_numbers = list(card_number_generator(start, end))
    assert len(card_numbers) == expected_count
    assert card_numbers[0] == f"{start:016}"
    assert card_numbers[-1] == f"{end:016}"
